// Escreva um programa para ler o ano de nascimento de uma pessoa e escrever uma mensagem que diga se ela poderá ou não votar este ano (não é necessário considerar o mês em que ela nasceu).                                                           




var data = parseInt(prompt("Por favor digite o seu ano de nascimento"));
var maioridade = 16;
var data_atual = 2024;


if (data_atual - data >= maioridade) [
    alert("Olá você poderá votar esse ano!")]

else{
    alert("Você não podera voltar esse ano!")
}
